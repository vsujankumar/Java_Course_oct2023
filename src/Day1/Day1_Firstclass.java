package Day1;

public class Day1_Firstclass {
	
	static int a;
	char b;
	double c;
	boolean d;

	public static void main(String[] args) {
		
		byte f;
		short g;
		f=12;
		g=22;
		
		System.out.println(f);
		
		
		Day1_Firstclass dfc=new Day1_Firstclass();
		
		
		System.out.println(dfc.a);
		System.out.println(dfc.b);
		System.out.println(dfc.c);
		System.out.println(dfc.d);
		System.out.println(f);

	}

}
